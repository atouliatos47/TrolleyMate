function translateAisleName(name) {
    const lang = localStorage.getItem('bm_language') || 'en';
    if (lang === 'en') return name;
    const map = AISLE_NAMES[lang] || {};
    // Extract only letters, numbers, spaces, & from the name
    const textOnly = name.replace(/[^a-zA-Z0-9\s&']/g, ' ').replace(/\s+/g, ' ').trim();
    if (map[textOnly]) {
        // Keep the original emoji prefix if present
        const emojiPrefix = name.match(/^[\s\S]*?(?=[A-Z])/)?.[0] || '';
        return emojiPrefix + map[textOnly];
    }
    return name;
}

function translateAisleName(name) {
    const lang = localStorage.getItem('bm_language') || 'en';
    if (lang === 'en') return name;
    const map = AISLE_NAMES[lang] || {};
    // Extract just the English word(s) from the name e.g. "Bakery" from "🥖 Bakery"
    const getWords = s => s.replace(/[^a-zA-Z0-9\s&']/g, ' ').replace(/\s+/g, ' ').trim();
    const nameWords = getWords(name);
    for (const [key, val] of Object.entries(map)) {
        const keyWords = getWords(key);
        if (keyWords === nameWords) return val;
    }
    return name;
}
